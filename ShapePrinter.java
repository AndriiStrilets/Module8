class ShapePrinter {

    public void printShapeName (Shape shape) {
        Shape r = new Rectangle();
        r.printName();

        Shape c = new Circle();
        c.printName();

        Shape q = new Quad();
        q.printName();

        Shape s = new Square();
        s.printName();

        Shape t = new Triangle();
        t.printName();

    }
}
